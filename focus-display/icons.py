# Icon dimensions
ICON_WIDTH = 24
ICON_HEIGHT = 24


def get_icon(meeting_type):
    """Get icon data for a meeting type."""
    icons = {
        "meet": ICON_MEET,
        "zoom": ICON_ZOOM,
        "teams": ICON_TEAMS,
        "in_person": ICON_LOCATION,
    }
    return icons.get(meeting_type)


def draw_icon(display, x, y, meeting_type):
    """
    Draw a meeting type icon at the given position.

    Args:
        display: Inkplate display object
        x, y: Top-left corner position
        meeting_type: 'meet', 'zoom', 'teams', or 'in_person'
    """
    icon = get_icon(meeting_type)
    if icon is None:
        return

    for row_idx, row_bytes in enumerate(icon):
        for byte_idx, byte_val in enumerate(row_bytes):
            for bit in range(8):
                if byte_val & (0x80 >> bit):
                    px = x + byte_idx * 8 + bit
                    py = y + row_idx
                    display.drawPixel(px, py, 1)
